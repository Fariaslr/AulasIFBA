    public void fillAllCombos() {

        SemestreDAO semestreDAO = new SemestreJDBC();
        List<Semestre> Semestres = semestreDAO.read();

        for (Semestre s : Semestres) {
            cboSemestre.addItem(s);
        }

        SemestreVigenteDAO semestreVigenteDAO = new SemestreVigenteJDBC();
        List<SemestreVigente> SemestresVigentes = semestreVigenteDAO.read();

        for (SemestreVigente s : SemestresVigentes) {
            cboSemestreVigente.addItem(s);
        }

        LocalDaAulaDAO localDaAulaDAO = new LocalDaAulaJDBC();
        List<LocalDaAula> locais = localDaAulaDAO.read();

        for (LocalDaAula l: locais) {
            cboLocalDaAula.addItem(l);
        }

    }

