    public void initTable() {
        DefaultTableCellRenderer rendererCentro = new DefaultTableCellRenderer();
        rendererCentro.setHorizontalAlignment(SwingConstants.CENTER);
        TableColumnModel modeloDaColuna = jTProfessores.getColumnModel();
        DefaultTableModel modelo = (DefaultTableModel) jTProfessores.getModel();
        jTProfessores.setRowSorter(new TableRowSorter(modelo));
        

        modeloDaColuna.getColumn(0).setMaxWidth(35);
        modeloDaColuna.getColumn(0).setCellRenderer(rendererCentro);
        modeloDaColuna.getColumn(0).setHeaderRenderer(rendererCentro);

        modeloDaColuna.getColumn(1).setMinWidth(300);
        modeloDaColuna.getColumn(1).setMaxWidth(350);
        modeloDaColuna.getColumn(1).setHeaderRenderer(rendererCentro);

        modeloDaColuna.getColumn(2).setHeaderRenderer(rendererCentro);
        modeloDaColuna.getColumn(2).setCellRenderer(rendererCentro);
        
        readJTable();
        fillComboProfessor();
    }

    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTProfessores.getModel();
        modelo.setNumRows(0);
        ProfessorDAO pDao = new ProfessorJDBC();

        for (Professor p : pDao.read()) {
            modelo.addRow(new Object[]{
                p.getCodigoProfessor(),
                p.getNomeProfessor(),
                p.getMatricula()
            });
        }
    }

    public void searchJTable(Professor prof) {
        DefaultTableModel modelo = (DefaultTableModel) jTProfessores.getModel();
        modelo.setNumRows(0);
        ProfessorDAO pDAO = new ProfessorJDBC();

        for (Professor p : pDAO.search(prof)) {
            modelo.addRow(new Object[]{
                p.getCodigoProfessor(),
                p.getNomeProfessor(),
                p.getMatricula()
            });
        }
    }

    public void fillComboProfessor() {
        ProfessorDAO ProfessorDAO = new ProfessorJDBC();
        List<Professor> Professores = ProfessorDAO.read();

        for (Professor p : Professores) {
            cbxProfessor.addItem(p);
        }
    }

