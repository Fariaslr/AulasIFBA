    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) JTHorarios.getModel();
        modelo.setNumRows(0);
        HorarioDAO hDao = new HorarioDAOJDBC();
        
        for (Horario h : hDao.read()){
            modelo.addRow(new Object[]{
                h.getCodigoHorario(),
                h.getDescricaoHorario()
            });
        }
    }

